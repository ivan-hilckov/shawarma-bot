#!/bin/bash
set -e

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è tar –∞—Ä—Ö–∏–≤–∞ –±–µ–∑ –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–π macOS
create_tar() {
    local archive_name="$1"
    local source_dir="$2"

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –û–° –∏ –∏—Å–ø–æ–ª—å–∑—É–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–µ —Ñ–ª–∞–≥–∏
    if [[ "$OSTYPE" == "darwin"* ]]; then
        # macOS - –ø–æ–¥–∞–≤–ª—è–µ–º extended attributes
        tar --no-xattrs -czf "$archive_name" "$source_dir"
    else
        # Linux - —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–∞—è –∫–æ–º–∞–Ω–¥–∞
        tar -czf "$archive_name" "$source_dir"
    fi
}

echo "üì∏ –°–∫—Ä–∏–ø—Ç –ø–µ—Ä–µ–Ω–æ—Å–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π –Ω–∞ —Å–µ—Ä–≤–µ—Ä"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è –ø–∞–ø–∫–∏ assets
if [ ! -d "assets" ]; then
    echo "‚ùå –ü–∞–ø–∫–∞ assets –Ω–µ –Ω–∞–π–¥–µ–Ω–∞ –≤ —Ç–µ–∫—É—â–µ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏"
    echo "–£–±–µ–¥–∏—Ç–µ—Å—å —á—Ç–æ –≤—ã –Ω–∞—Ö–æ–¥–∏—Ç–µ—Å—å –≤ –∫–æ—Ä–Ω–µ –ø—Ä–æ–µ–∫—Ç–∞"
    exit 1
fi

# –ü–æ–¥—Å—á–µ—Ç —Ñ–∞–π–ª–æ–≤
IMAGES_COUNT=$(ls assets/*.jpeg 2>/dev/null | wc -l)
if [ $IMAGES_COUNT -eq 0 ]; then
    echo "‚ùå –í –ø–∞–ø–∫–µ assets –Ω–µ –Ω–∞–π–¥–µ–Ω–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π .jpeg"
    exit 1
fi

echo "‚úÖ –ù–∞–π–¥–µ–Ω–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π: $IMAGES_COUNT"

# –ó–∞–ø—Ä–æ—Å –¥–∞–Ω–Ω—ã—Ö —Å–µ—Ä–≤–µ—Ä–∞
read -p "–í–≤–µ–¥–∏—Ç–µ –∞–¥—Ä–µ—Å —Å–µ—Ä–≤–µ—Ä–∞ (IP –∏–ª–∏ –¥–æ–º–µ–Ω): " SERVER_HOST
read -p "–í–≤–µ–¥–∏—Ç–µ –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è: " SERVER_USER
read -p "–í–≤–µ–¥–∏—Ç–µ –ø—É—Ç—å –∫ –ø—Ä–æ–µ–∫—Ç—É –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ [~/shawarma-bot]: " SERVER_PATH
SERVER_PATH=${SERVER_PATH:-~/shawarma-bot}

echo ""
echo "üîç –ü–∞—Ä–∞–º–µ—Ç—Ä—ã –ø–µ—Ä–µ–Ω–æ—Å–∞:"
echo "–°–µ—Ä–≤–µ—Ä: $SERVER_USER@$SERVER_HOST"
echo "–ü—É—Ç—å: $SERVER_PATH"
echo "–§–∞–π–ª–æ–≤: $IMAGES_COUNT"
echo ""

read -p "–ü—Ä–æ–¥–æ–ª–∂–∏—Ç—å? (y/N): " CONFIRM
if [[ ! $CONFIRM =~ ^[Yy]$ ]]; then
    echo "‚ùå –û—Ç–º–µ–Ω–µ–Ω–æ"
    exit 1
fi

# –°–æ–∑–¥–∞–Ω–∏–µ –∞—Ä—Ö–∏–≤–∞
echo "üì¶ –°–æ–∑–¥–∞–Ω–∏–µ –∞—Ä—Ö–∏–≤–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π..."
create_tar assets-backup.tar.gz assets/
echo "‚úÖ –ê—Ä—Ö–∏–≤ —Å–æ–∑–¥–∞–Ω: assets-backup.tar.gz ($(du -h assets-backup.tar.gz | cut -f1))"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ SSH –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è
echo "üîê –ü—Ä–æ–≤–µ—Ä–∫–∞ SSH –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è..."
if ! ssh -o ConnectTimeout=10 -o BatchMode=yes $SERVER_USER@$SERVER_HOST exit 2>/dev/null; then
    echo "‚ùå –ù–µ —É–¥–∞–µ—Ç—Å—è –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ —Å–µ—Ä–≤–µ—Ä—É –ø–æ SSH"
    echo "–£–±–µ–¥–∏—Ç–µ—Å—å —á—Ç–æ:"
    echo "1. SSH –∫–ª—é—á–∏ –Ω–∞—Å—Ç—Ä–æ–µ–Ω—ã"
    echo "2. –°–µ—Ä–≤–µ—Ä –¥–æ—Å—Ç—É–ø–µ–Ω"
    echo "3. –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∏ –∞–¥—Ä–µ—Å —É–∫–∞–∑–∞–Ω—ã –ø—Ä–∞–≤–∏–ª—å–Ω–æ"
    rm assets-backup.tar.gz
    exit 1
fi
echo "‚úÖ SSH –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ —Ä–∞–±–æ—Ç–∞–µ—Ç"

# –ü–µ—Ä–µ–Ω–æ—Å –∞—Ä—Ö–∏–≤–∞ –Ω–∞ —Å–µ—Ä–≤–µ—Ä
echo "üì§ –ó–∞–≥—Ä—É–∑–∫–∞ –∞—Ä—Ö–∏–≤–∞ –Ω–∞ —Å–µ—Ä–≤–µ—Ä..."
scp assets-backup.tar.gz $SERVER_USER@$SERVER_HOST:$SERVER_PATH/
echo "‚úÖ –ê—Ä—Ö–∏–≤ –∑–∞–≥—Ä—É–∂–µ–Ω"

# –†–∞—Å–ø–∞–∫–æ–≤–∫–∞ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ
echo "üìÇ –†–∞—Å–ø–∞–∫–æ–≤–∫–∞ –∞—Ä—Ö–∏–≤–∞ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ..."
ssh $SERVER_USER@$SERVER_HOST << EOF
cd $SERVER_PATH

# –°–æ–∑–¥–∞–Ω–∏–µ –±—ç–∫–∞–ø–∞ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏—Ö –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π (–µ—Å–ª–∏ –µ—Å—Ç—å)
if [ -d "assets" ]; then
    echo "üìã –°–æ–∑–¥–∞–Ω–∏–µ –±—ç–∫–∞–ø–∞ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏—Ö –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π..."
    tar -czf assets-backup-old-\$(date +%Y%m%d_%H%M%S).tar.gz assets/
fi

# –†–∞—Å–ø–∞–∫–æ–≤–∫–∞ –Ω–æ–≤—ã—Ö –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π
echo "üìÇ –†–∞—Å–ø–∞–∫–æ–≤–∫–∞ –Ω–æ–≤—ã—Ö –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π..."
tar -xzf assets-backup.tar.gz

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
if [ -d "assets" ]; then
    EXTRACTED_COUNT=\$(ls assets/*.jpeg 2>/dev/null | wc -l)
    echo "‚úÖ –†–∞—Å–ø–∞–∫–æ–≤–∞–Ω–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π: \$EXTRACTED_COUNT"

    # –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –ø—Ä–∞–≤ –¥–æ—Å—Ç—É–ø–∞
    chmod 644 assets/*.jpeg
    echo "‚úÖ –ü—Ä–∞–≤–∞ –¥–æ—Å—Ç—É–ø–∞ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã"

    # –£–¥–∞–ª–µ–Ω–∏–µ –∞—Ä—Ö–∏–≤–∞
    rm assets-backup.tar.gz
    echo "‚úÖ –í—Ä–µ–º–µ–Ω–Ω—ã–π –∞—Ä—Ö–∏–≤ —É–¥–∞–ª–µ–Ω"
else
    echo "‚ùå –û—à–∏–±–∫–∞ —Ä–∞—Å–ø–∞–∫–æ–≤–∫–∏"
    exit 1
fi
EOF

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
echo "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞..."
REMOTE_COUNT=$(ssh $SERVER_USER@$SERVER_HOST "ls $SERVER_PATH/assets/*.jpeg 2>/dev/null | wc -l")

if [ "$REMOTE_COUNT" -eq "$IMAGES_COUNT" ]; then
    echo "‚úÖ –ü–µ—Ä–µ–Ω–æ—Å –∑–∞–≤–µ—Ä—à–µ–Ω —É—Å–ø–µ—à–Ω–æ!"
    echo "üìä –õ–æ–∫–∞–ª—å–Ω–æ: $IMAGES_COUNT —Ñ–∞–π–ª–æ–≤"
    echo "üìä –ù–∞ —Å–µ—Ä–≤–µ—Ä–µ: $REMOTE_COUNT —Ñ–∞–π–ª–æ–≤"
else
    echo "‚ö†Ô∏è –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ñ–∞–π–ª–æ–≤ –Ω–µ —Å–æ–≤–ø–∞–¥–∞–µ—Ç"
    echo "üìä –õ–æ–∫–∞–ª—å–Ω–æ: $IMAGES_COUNT —Ñ–∞–π–ª–æ–≤"
    echo "üìä –ù–∞ —Å–µ—Ä–≤–µ—Ä–µ: $REMOTE_COUNT —Ñ–∞–π–ª–æ–≤"
fi

# –£–¥–∞–ª–µ–Ω–∏–µ –ª–æ–∫–∞–ª—å–Ω–æ–≥–æ –∞—Ä—Ö–∏–≤–∞
rm assets-backup.tar.gz
echo "‚úÖ –õ–æ–∫–∞–ª—å–Ω—ã–π –∞—Ä—Ö–∏–≤ —É–¥–∞–ª–µ–Ω"

echo ""
echo "üéâ –ò–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è —É—Å–ø–µ—à–Ω–æ –ø–µ—Ä–µ–Ω–µ—Å–µ–Ω—ã –Ω–∞ —Å–µ—Ä–≤–µ—Ä!"
echo ""
echo "üìã –°–ª–µ–¥—É—é—â–∏–µ —à–∞–≥–∏:"
echo "1. –ó–∞–ø—É—Å—Ç–∏—Ç–µ –±–æ—Ç–∞ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ: docker-compose up -d"
echo "2. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Ä–∞–±–æ—Ç—É –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π –≤ Telegram"
echo "3. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ª–æ–≥–∏: docker-compose logs -f bot"
echo ""
echo "üîß –î–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —Å–æ—Å—Ç–æ—è–Ω–∏—è –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ:"
echo "ssh $SERVER_USER@$SERVER_HOST 'cd $SERVER_PATH && ./health-check.sh'"
